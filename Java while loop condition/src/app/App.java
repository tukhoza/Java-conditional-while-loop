package app;

public class App {

	public static void main(String[] args) {
		
		int count1 = 0;
		
		while (count1 < 10){
			
			System.out.println("The increment number :" + count1);
			count1 = count1 + 1;
		}
		
		
		System.out.println("\nPrint numbers from 1 to 10 \n");
		int count2 = 1;
		
		while (count2 <= 10){
			
			System.out.println("Number : " + count2);
			count2 += 1;
			
		}
	}

}
